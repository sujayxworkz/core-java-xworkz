class IndianRupee{

 private int rupeeId;
 private int noOfLanguages;
  private long serialNumber;
 private String rupeeName;
  private String colour;
 private String backSidePicture;
 private String location;
 
 public void setRupeeId(int rupeeId){
	 this.rupeeId = rupeeId;
 }
 public int getRupeeId(){
	 return rupeeId;
 }
 public void setNoOfLanguages(int noOfLanguages){
	 this.noOfLanguages = noOfLanguages;
 }
 public int getNoOfLanguages(){
	 return noOfLanguages;
 }
 public void setSerialNumber(long serialNumber){
	 this.serialNumber = serialNumber;
 }
 public long getSerialNumber(){
	 return serialNumber;
 }
 public void setRupeeName(String rupeeName){
	 this.rupeeName = rupeeName;
 }
 public String getRupeeName(){
	 return rupeeName;
 }
 public void setColour(String colour){
	 this.colour = colour;
 }
 public String getColour(){
	 return colour;
 }
 public void setBackSidePicture(String backSidePicture){
	 this.backSidePicture = backSidePicture;
 }
 public String getBackSidePicture(){
	 return backSidePicture;
 }
 public void setLocation(String location){
	 this.location = location;
 }
 public String getLocation(){
	 return location;
 }




}